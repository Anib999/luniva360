          <p>I had an enjoyable and truly enriched learning experience while working as an intern at Luniva Technology. I will briefly describe my internship experiences.</p>
          <p>As I was a fresh BE Computer Graduate and was in dilemma about my future, I got the chance to do an internship as a PHP developer at Luniva Tech. At first, I was a noob in this field and had no confidence in my work. I was constantly monitored and guided by the seniors to learn and increase my programming skills. Everyone was approachable and would readily address my questions and queries; this helped me in enriching my understanding of the business significantly. I also learned about the importance of teamwork in the successful execution of assignments. I also learned a great deal about the software development process and the different approaches to software development. I also committed some unintentional mistakes and learned from them.</p>
          <p>While working here we had to participate in regular stand-up meetings every day. Where we have to tell our colleagues and seniors about the current work that one’s engaged in. And it was really helpful as everyone will know about what others are doing and I never had to go out of work as I used to be assigned with work if I was free.</p>
          <p>We also had team building activities on Sunday, where we have to play games dividing the office staff into teams. It used to be great fun as well as helps to keep strong bonding with other colleagues and seniors. It also helps to get rid of work stress and keep a healthy working environment.</p>
          <p>Every Friday, we used to have a team meeting where the projects, progress, and problems are discussed and everyone gets a chance to complain about their dissatisfaction and discomfort encountered while working, directly to whom it concern.</p>
          <p>As a never-before full-time worker and unfamiliar with the working environment, I had a great deal to learn and many more things to experience. With the height of the monsoon season pounding on our windows and the strict deadlines of work pounding on our minds, the challenges are going to continue presenting themselves. I don’t know how fast these three months had passed away as if the time’s on drugs. In this journey of internship, I made lots of good friends, and their company and supports are truly appreciable. Those fun memories of working and playing games with my co-workers are in-erasable and never to be forgotten.</p>
          <p>Overall, working as an intern was a great opportunity because I was able to combine academic knowledge with real-life experience. It was also an excellent chance to network with professionals and gain contacts in the business world. I’ll always be grateful and will be in debt for what Luniva Tech has done for me.</p>

