            <h4>Hiking To Namo Boudha- Divya Bhattarai</h4>
          </div>
          <p>
          It was the early Saturday morning of 5th July 2014, with very calm and indifferent weather. As I missed my alarm, I thought I was going to be late for my very first hike with Luniva Tech family but I was quite wrong.  Rajiv Sir and his group were last to arrive at the departure stop. The weather was a bit gloomy and few drops of rain also drizzled down at our departure stop but we were still hopeful that we could complete our hike. Being delayed by 1 hour we finally headed towards our destination with a group of 6 at 7 am. The other 2 were picked up from Bhaktapur for their easiness and our journey officially started with all the members.

          </p>
          <p>At 9 Am we finally reached the starting point of our hike, Dhulikhel, which means we had to walk from there. Anita and kushma were very angry with Gokul as Gokul didn‚Äôt give them the Chocolate they asked for at Bus and I think they are still angry with him. üôÇ</p>
          <p>After around 10 minutes of walk, we had our breakfast at Dhulikhel. After that, we buckled up our shoes, grabbed our bags, and started our hike. We then rolled. We started to climb the stairs; at first, it seemed easy but slowly we got divided into two groups between the ones who could easily climb fast hikers and the ones who were slow. I along with few others fell on the latter one.Kushma helped Gokul with his bag as his bag containing all the goods required for the hike.</p>
          <p>The weather favored us; the temperature was not so high. We were all sweating buckets by the time we reached Shanti Ban. We rested there for a very short time and then headed for Kali Temple. By then Anita was very tired and stayed at bottom of the temple but we Climbed and returned not delaying a single second. The view from the top of the temple was foggy, still very pleasant which Anita missed.</p>
          <p>On continuation of our journey, we came across a breathtaking, green Chour and fog; the combination was awesome. We ate chocolate and clicked pictures by posing, jumping, and acting. It poured us with more energy and that place was ‚ÄúSarada Batase dada‚Äù. We had to reach Shankhu from Batase dada to view our very own Nhuren Sir‚Äôs birthplace the way was slanting down from there. By then all were hungry and all started to blame Nhuren Sir (organizer) for taking us on the long route.  After a while we reached shankhu but we hadn‚Äôt reach Nhuren sir‚Äôs home till then. The way from there was straight. We three; Kushma, Anita, and I walked without shoes to comfort our legs and it felt good and relieved. At one time we came across a snake, which crossed paths with Rajiv Sir. After some walk, we finally reached a very peaceful and comfortable place for all of us at that time which was Nhuren Sir‚Äôs home at 1 pm. We had lunch there and it was simply awesome. We received very good courtesy and hospitality there and we want to thank Nhuren Sir and his family for that.</p>
          <p>Resting for a while we resumed our hike to reach our final destination, Namoboudha.  It was the steep hill from there to reach the peak. All were tired by then but Rajiv Sir and Sandhya di seemed to have more energy than others. They were leading in the group and we all fell back. Anita was struggling, Kushma and I were singing in that difficult situation to hide the pain and Gokul was helping us by choosing the song. We were not walking but crawling like a monkey. Gokul had said that he couldn‚Äôt walk for more than half an hour but he seemed energetic and was walking constantly and courageously. All were sweating but we all managed to climb taking rest from time to time. After a walk of around 2 hours, we reached our destination. As soon as we reached there we all felt blessed and refilled with all the energy that was lost during the journey. The view from the top, Oh God I can‚Äôt describe it in words, It was just breathtaking.</p>
          <p>The gumba was very peaceful and holy. It is believed that this gumba was made to respect and represent the legendary story of Lord Buddha, who came back from his journey to feed a hungry tiger, where he ended up feeding the tiger flesh of his own hands. We had to open our shoes on the gumba premises. We clicked lots of pictures, felt the divine peaceful power that resided in the gumba. The view was indescribably beautiful. But since we had to catch the bus at 3:30 pm we started to head back to the village. On our way back, some of us did the ‚ÄúTarzan Show‚Äù on a tree root, where Anita and Gokul hurt their hands. At first, we were walking at our own pace. Then nhuren Sir said he will be going first so as to catch the bus. He started running down the steep hill. Later Sandhya di, Rajiv Sir, and Kushma followed too and ran like a bear was chasing them. We also started running when we got a call from Nhuren Sir telling us the bus is about to leave. Finally, we caught the bus and the comfort of the seat was heaven. We then dropped at Panauti temple, clicked pictures, had titaura. Anita chased the ducks away in the name of the photo session. Poor ducks were scared to death. Finally, we headed home. Anita and Kushma dropped at Bhaktapur, me at Koteswhor whereas Gokul went along with the rest of the team and had a great deal of fancy food and beverage.</p>
          <p>That was my best experience of hiking with the amiable Luniva Tech family.</p>

