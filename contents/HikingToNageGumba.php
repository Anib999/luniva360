          <div class="title-heading">
            <h4>Hiking To Nage Gompa -Kushma Thapa</h4>
          </div>
          <p>My morning started with a visit to mahankal bhairab. Then, four of us met at the NAC stop, and since we had to wait for the other 3, we decided to give a visit to sankata mandir. After the half, an hour or so all seven of us had gathered, and in the meantime, while waiting for the microbus, we started to play the famous 90’s game “S.T.O.P.”. People around us were probably calling us crazy.
          The micro dropped us to budhanilkantha chowk. And we started our hiking thereafter. While we were walking in the jungle of Shivapuri National Conservation Area, we decided to take the shortcut routes once in a while just for fun, where we literally had to climb using the tree roots and branches.</p>
          <p>After hours of walking, we finally reached our destination, Nage Gompa. The old chaur had turned to a six-story building. The view from therein was amazing, breathtaking. We settled in the grasses and started making our breakfast. It started getting too windy and cold. We clicked pictures, did our selfies and group snaps. Then the dilemma arrived, which was either to go forward or to stop. After too much discussion we finally decided to move forward. So we started to climb deeper into the woods. After an hour or so, it started to rain. The paths were quite a thing. And the drizzle was sure to make it difficult to return, so we stopped, had our energy drinks, and started to go back. Luckily for us, the paths had not turned too muddy or slippery. We stopped at a local shop and ordered tea.</p>
          <p>As we were sipping our tea, something amazing happened. The fog started coming down. And it looked so amazing, not less than the first snowfall experience. We all started jumping and taking pictures. And in a few minutes, everything around turned whitish.</p>
          <p>We started heading down. And on our way, we played games, and anybody who made a mistake had to dance. It was quite fun to see a guy dancing in Bollywood item number. We took a micro to the new road, ate tons, and shared our scary moments. And then the journey ended.</p>
          
