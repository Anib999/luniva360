          <div class="title-heading">
            <h4>Hiking To nage Gumba- Anita Suwal</h4>
          </div>
          <p>
          We planned to meet at NAC at 6:00 a.m. I was the first person to say that but I reached at 8:00 am. Our journey started at that moment. We were waiting for the bus. And at the same time, we played the game “stop” at the bus stop. All of them enjoyed it but me because I was out in the first round as I did not know the rule.
          </p>
          <p>
          The Bus didn’t come so we chose the next route, the route to Budhanilkantha. From that point, the actual hiking started. We started walking. After walking for about an hour, we reached Shivapuri Nagarjun National Park gate. Sarose (colleague) went to buy tickets and we started doing a photo session. There we met international tourists. The tourists were approaching us to take their photos. We did and asked them to take a photo of ours also.
            We started our journey to Nage Gumba. I was the weaker person on the team. All of them look so energetic but I…. I had eaten two chocolates(Snickers) already cause I felt hungry and weak. From time to time I was saying I am hungry, I can’t walk. Clicking photos, singing songs, eating snickers, drinking tang juice... We reached Nage Gumba. I was so tired and hungry that instantly I wanted to have food but others (team) were busy clicking photos. While Clicking the photo Kushma(colleague) started flirting with a tourist name Cyrus and Bikash too started flirting with a tourist named Laila. After a while, breakfast started. I ate as if I was never given food to eat.
          </p>
          <p>We decided to hike up to Shivapuri despite the disagreement from me and Rajiv Sir. We started but after a while rainfall. Due to the rain, we decided to return back. We returned to the Gumba. There we had black tea which was so tasty. We enjoyed the fog. Clicked lots of photos, danced, played the game, jumped. We returned singing songs. I didn’t sing a song as I am not good at all. Then Rajiv Sir gave us an idea to play the game “word puzzle”. We twist the game by instead of words of everything, we choose only person name. There was punishment for those who cannot say the words. The punishment was dance. By this way we reached Budhanilkantha and we ride the bus, reached New Road where we had dinner and after that, we all went to our own home.</p>
          <p>The hiking was so exciting, more enjoyable, joyful, and memorable. I enjoyed it a lot. Thanks to all my team for making the hiking remarkable.</p>

