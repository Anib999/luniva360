          <p>Yesterday, we had a team-building activity in our office. Like other times, this time it wasn’t well organized. But that was the fun part. Because it wasn’t well organized, we ended up rolling on the floor laughing.</p>
          <p>The First Round was a dialogue competition, where the participants shouted out the dialogues of movies “loot”, “3 idiots”.</p>
          <p>In the Second Round, one participant from each team was called forward and asked questions related to their opponent and the office building. And the Third Round was the “Antonym” round where the teams were supposed to act the opposite of the actions that has been asked to do.
          There was too much confusion in this round like is the opposite of the action “run” and “stop”, walk?</p>
          <p>It was so confusing to decide which is the winner team that we held a tug of war round.ting where the projects, progress, and problems are discussed and everyone gets a chance to complain about their dissatisfaction and discomfort encountered while working, directly to whom it concern.</p>
          <p>At the end of the team building activity it was decided that the award “chocolates” will be thrown in the air and the participants should try to collect as many as they can. That was the funniest part of the whole game.</p>
          <p>Co-incidentally, yesterday was the so-called “chocolates day-February 9th”, and well we fought for chocolates.</p>

